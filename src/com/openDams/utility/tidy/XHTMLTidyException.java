package com.openDams.utility.tidy;

public class XHTMLTidyException extends Exception {

	public XHTMLTidyException() {
		super();
	}

	public XHTMLTidyException(String message) {
		super(message);
	}

	public XHTMLTidyException(Throwable cause) {
		super(cause);
	}

	public XHTMLTidyException(String message, Throwable cause) {
		super(message, cause);
	}

}
