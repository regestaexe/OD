package com.openDams.documental.exporters.mets;

public class MetsException extends Exception {

	public MetsException() {
		super();
	}

	public MetsException(String message) {
		super(message);
	}

	public MetsException(Throwable throwable) {
		super(throwable);
	}

	public MetsException(String message, Throwable throwable) {
		super(message, throwable);
	}

}
